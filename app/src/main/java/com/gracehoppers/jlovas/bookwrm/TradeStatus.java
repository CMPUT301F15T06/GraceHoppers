/**
 * This class is an enum for the trade types. Each number corresponds to a
 * type of book.
 *
 * @Author Jillian Lovas
 *
 * @see Trade
 */
public enum TradeStatus {
    /**
     * Enumeration where each item describes the state of a trade.
     */
    ACCEPTED, DECLINED, COUNTERED;
}
